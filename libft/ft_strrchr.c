char	*ft_strrchr(const char *s, int c)
{
	char	*p;
	char	ch;

	p = (char *) s;
	ch = (char) c;
	while (*p)
		p++;
	while ((p != s) && *p != ch)
		p--;
	if (*p != ch)
		p = 0;
	return (p);
}
